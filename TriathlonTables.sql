CREATE TABLE Team (
	teamNo int NOT NULL PRIMARY KEY,
	teamName varchar(25) NOT NULL
);


CREATE TABLE Competitor (
	competitorNo int NOT NULL PRIMARY KEY,
	forename varChar(15) NOT NULL,
	surname varchar(20) NOT NULL,
	town varchar(20) NOT NULL,
	contactNo varchar(11) NOT NULL,
	teamNo int NOT NULL,
	FOREIGN KEY (teamNo) REFERENCES Team(teamNo)
);


CREATE TABLE Race(
	raceNo int NOT NULL PRIMARY KEY,
	raceName varchar(30) NOT NULL,
	raceDate date NOT NULL
);


CREATE TABLE Results (
	raceNo int NOT NULL,
	competitorNo int NOT NULL,
	timeTaken time(7) NOT NULL,
	position int NOT NULL,
	PRIMARY KEY (raceNo, competitorNo),
	FOREIGN KEY (raceNo) REFERENCES Race(raceNo),
	FOREIGN KEY (competitorNo) REFERENCES Competitor(competitorNo)
);

